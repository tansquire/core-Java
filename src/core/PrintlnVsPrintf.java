
package core;

public class PrintlnVsPrintf 
{
    public static void main(String[] args) 
    {
        int a,b,c;
        a=2;
        b=3;
        c=a+b;
        System.out.println("addition of "+a+" and "+b+" is "+c);
        System.out.printf("addition of %d and %d is %d",a,b,c);
        
    }
    
}
