public class Switch_Case_String 
{
    public static void main(String[] args) 
    {
        String i="hi";
        switch(i)
        {
            case "hi":
                System.out.println("hi");
                break;
            case "hello":
                System.out.println("hello");
                break;
            case "what":
                System.out.println("what");
                break;
            default:
                System.out.println("Nothing");
                
        }
        
    }
    
}
