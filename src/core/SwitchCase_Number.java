
package core;


public class SwitchCase_Number 
{
    public static void main(String[] args) 
    {
        int i=9;
        switch(i)
        {
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("two");
                break;
            case 3:
                System.out.println("three");
                break;
            default:
                System.out.println("Nothing");
                
        }
        
    }
    
}
